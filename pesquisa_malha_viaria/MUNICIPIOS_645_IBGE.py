def obter_municipios_645() -> list:
    """Retorna lista de 645 municípios únicos de SP (IBGE)"""
    # Remover duplicatas mantendo ordem
    municipios_unicos = []
    vistos = set()
    
    for municipio in MUNICIPIOS_645_IBGE:
        municipio_limpo = municipio.strip()
        if municipio_limpo and municipio_limpo not in vistos:
            municipios_unicos.append(municipio_limpo)
            vistos.add(municipio_limpo)
    
    # Garantir 645 municípios
    if len(municipios_unicos) < 645:
        print(f"⚠️  Aviso: Lista contém {len(municipios_unicos)} municípios (esperado 645)")
    
    return municipios_unicos[:645]

